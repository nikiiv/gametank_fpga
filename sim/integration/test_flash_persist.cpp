// Flash-write emulation test (persist.c sequences): the SDK save layer
// erases a sector ($30 with toggle-polling) and AND-programs bytes ($A0)
// through the banked window; the emulator implements this command set on
// the FLASH2M image, and games probe/write saves at boot — without it
// their boot state forks (Ganymede M8). Runs the exact SDK idiom against
// the 8 KB sector at image 0x1FA000 (bank $7E, window bit13=1).

#include "sim_harness.h"

int main() {
    Sim sim;

    std::vector<uint8_t> img(2 * 1024 * 1024, 0xFF);
    const uint32_t FB = 0x1FC000;
    static const uint8_t prog[] = { 0x78, 0xD8, 0xA2, 0xFF, 0x9A, 0x9C, 0x05, 0x20, 0xA9, 0x07, 0x8D, 0x03, 0x28, 0x9C, 0x01, 0x28, 0xA9, 0x00, 0x8D, 0x01, 0x28, 0xA9, 0x01, 0x8D, 0x01, 0x28, 0xA9, 0x02, 0x8D, 0x01, 0x28, 0xA9, 0x03, 0x8D, 0x01, 0x28, 0xA9, 0x02, 0x8D, 0x01, 0x28, 0xA9, 0x03, 0x8D, 0x01, 0x28, 0xA9, 0x02, 0x8D, 0x01, 0x28, 0xA9, 0x03, 0x8D, 0x01, 0x28, 0xA9, 0x02, 0x8D, 0x01, 0x28, 0xA9, 0x03, 0x8D, 0x01, 0x28, 0xA9, 0x02, 0x8D, 0x01, 0x28, 0xA9, 0x03, 0x8D, 0x01, 0x28, 0xA9, 0x02, 0x8D, 0x01, 0x28, 0xA9, 0x03, 0x8D, 0x01, 0x28, 0xA9, 0x00, 0x8D, 0x01, 0x28, 0xA9, 0x01, 0x8D, 0x01, 0x28, 0x9C, 0x01, 0x28, 0xA9, 0x04, 0x8D, 0x01, 0x28, 0x9C, 0x01, 0x28, 0xAD, 0x23, 0xA1, 0x85, 0x10, 0xA9, 0xAA, 0x8D, 0xAA, 0x8A, 0xA9, 0x55, 0x8D, 0x55, 0x85, 0xA9, 0x80, 0x8D, 0xAA, 0x8A, 0xA9, 0xAA, 0x8D, 0xAA, 0x8A, 0xA9, 0x55, 0x8D, 0x55, 0x85, 0xA9, 0x30, 0x8D, 0x00, 0xA0, 0xAD, 0x00, 0xA0, 0x85, 0x11, 0xAD, 0x00, 0xA0, 0xC5, 0x11, 0xD0, 0xF4, 0xAD, 0x23, 0xA1, 0x85, 0x12, 0xA9, 0xA0, 0x8D, 0xAA, 0x8A, 0xA9, 0x5A, 0x8D, 0x23, 0xA1, 0xAD, 0x23, 0xA1, 0x85, 0x11, 0xAD, 0x23, 0xA1, 0xC5, 0x11, 0xD0, 0xF4, 0xAD, 0x23, 0xA1, 0x85, 0x13, 0xA9, 0xA0, 0x8D, 0xAA, 0x8A, 0xA9, 0x0F, 0x8D, 0x23, 0xA1, 0xAD, 0x23, 0xA1, 0x85, 0x11, 0xAD, 0x23, 0xA1, 0xC5, 0x11, 0xD0, 0xF4, 0xAD, 0x23, 0xA1, 0x85, 0x14, 0xAD, 0x23, 0x81, 0x85, 0x15, 0xA9, 0xC3, 0x85, 0x1F, 0x4C, 0xDE, 0xC3 };
    std::copy(prog, prog + sizeof(prog), img.begin() + FB + 0x300);
    img[FB + 0x3FFA] = 0xDE; img[FB + 0x3FFB] = 0xC3;
    img[FB + 0x3FFC] = 0x00; img[FB + 0x3FFD] = 0xC3;
    img[FB + 0x3FFE] = 0xDE; img[FB + 0x3FFF] = 0xC3;

    // pre-erase content in the target sector and its neighbor
    for (int i = 0; i < 0x2000; i++) img[0x1FA000 + i] = (uint8_t)(0x20 + (i & 0x3F));
    for (int i = 0; i < 0x2000; i++) img[0x1F8000 + i] = (uint8_t)(0x60 + (i & 0x1F));

    sim.gtrDownloadSparse(img);
    sim.reset();

    uint64_t guard = sim.cycles + 30000000ull;
    while (sim.sysram(0x001F) != 0xC3 && sim.cycles < guard) sim.tick();
    CHECK(sim.sysram(0x001F) == 0xC3, "cart did not finish");

    CHECK(sim.sysram(0x10) == img[0x1FA123], "pre-erase read: %02x want %02x",
          sim.sysram(0x10), img[0x1FA123]);
    CHECK(sim.sysram(0x12) == 0xFF, "post-erase read: %02x want FF",
          sim.sysram(0x12));
    CHECK(sim.sysram(0x13) == 0x5A, "programmed byte: %02x want 5A",
          sim.sysram(0x13));
    CHECK(sim.sysram(0x14) == 0x0A, "AND-programmed byte: %02x want 0A",
          sim.sysram(0x14));
    CHECK(sim.sysram(0x15) == img[0x1F8123], "neighbor sector: %02x want %02x",
          sim.sysram(0x15), img[0x1F8123]);

    std::printf("PASS flash_persist: sector erase + AND-program via the SDK "
                "save idiom\n");
    return 0;
}
